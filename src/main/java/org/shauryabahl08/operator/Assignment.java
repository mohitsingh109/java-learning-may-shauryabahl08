package org.shauryabahl08.operator;

public class Assignment {

    public static void main(String[] args) {
        // =, +=, -=, *=, /=, %=
        int amount = 10;
        // increment the amount by 5
        // amount = amount + 5;
        amount += 5; // amount = amount + 5;
        System.out.println("Account + 5 = " + amount);
        amount -= 2;
        System.out.println("Account - 2 = " + amount);

    }
}
