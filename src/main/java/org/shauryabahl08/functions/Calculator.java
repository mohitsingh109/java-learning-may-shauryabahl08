package org.shauryabahl08.functions;

public class Calculator {

    public static int add(int a, int b) {
        int result = a + b;
        return result; // 60
    }

    public static float addFloat(float a, float b, float c) {
        float result = a + b + c;
        return result;
    }

    public static int sub(int a, int b) {
        int result = a - b;
        return result;
    }
}
